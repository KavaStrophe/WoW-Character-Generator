
export class AgeOfRace {
    endOfChildhoodAge: number;
    endOfTeenagehoodAge: number;
    endOfAdulthoodAge: number;
    endOfSeniorhoodAge: number;

    constructor(endOfChildhoodAge: number, endOfTeenagehoodAge: number, endOfAdulthoodAge: number, endOfSeniorhoodAge: number) {
        this.endOfAdulthoodAge = endOfAdulthoodAge;
        this.endOfChildhoodAge = endOfChildhoodAge;
        this.endOfSeniorhoodAge = endOfSeniorhoodAge;
        this.endOfTeenagehoodAge = endOfTeenagehoodAge;
    }
}
